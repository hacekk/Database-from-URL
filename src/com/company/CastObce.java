package com.company;

public class CastObce {

    private String kod, nazev, kodObce;

    public CastObce(String kod, String nazev, String kodObce) {
        this.kod = kod;
        this.nazev = nazev;
        this.kodObce = kodObce;
    }

    public String getKod() {
        return kod;
    }

    public String getNazev() {
        return nazev;
    }

    public String getKodObce() {
        return kodObce;
    }


}
